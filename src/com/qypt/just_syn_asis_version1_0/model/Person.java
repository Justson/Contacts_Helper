public class Person {

	
	private String name;
	private String phone;
	private String phoneRe;
	private String nameRe;
	private int count;
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getPhoneRe() {
		return phoneRe;
	}
	public void setPhoneRe(String phoneRe) {
		this.phoneRe = phoneRe;
	}
	public String getNameRe() {
		return nameRe;
	}
	public void setNameRe(String nameRe) {
		this.nameRe = nameRe;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
}
